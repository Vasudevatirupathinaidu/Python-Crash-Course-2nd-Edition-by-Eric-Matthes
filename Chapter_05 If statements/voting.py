# Simple if statements
# if conditional_test:
#     do something

age = 19
if age >= 18:
    print("You are older enough to vote!")
    print("Have you registered to vote yet?\n")


# if-else statements
age = 17
if age >= 18:
    print("You are older enough to vote!")
    print("Have you registered to vote yet?\n")
else:
    print("Sorry, you are too young to vote.")
    print("Please register to vote as soon as you turn 18!\n")